export function getLangExtFromIndex(idx: number) {
  switch (idx) {
    case 0:
      return ".c";
    case 1:
      return ".cpp";
    case 2:
      return ".cpp";
    case 3:
      return ".java";
    case 4:
      return ".py";
    case 5:
      return ".py";
    case 6:
      return ".cs";
  }
}
